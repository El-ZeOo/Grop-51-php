<?php


$day = 'wednesday';
switch ($day) {
    case 'saturday':
        echo 'this is the saturday';
        break;
    case 'sunday':
        echo 'this is the sunday';
        break;
    case 'monday':
        echo 'this is the monday';
        break;
    case 'tuesday':
        echo 'this is the tuesday';
        break;
    case 'wednesday':
        echo 'this is the wednesday';
        break;
    case 'thursday':
        echo 'this is the thursday';
        break;
    case 'friday':
        echo 'this is the friday';
        break;

    default:
        echo 'this is not a day';
        break;
}
